public class ViDu {
    public static int tinhTong(int a, int b) {
        return a + b;
    }

    public static int tinhHieu(int a, int b) {
        return a - b;
    }

    public static int tinhTich(int a, int b) {
        return a * b;
    }

    public static float tinhThuong(int a, int b) {
        return (float) a / b;
    }

    public static int tinhDu(int a, int b) {
        return a % b;
    }
}
